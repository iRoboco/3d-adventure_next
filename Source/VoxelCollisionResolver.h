#pragma once
#include "axmol.h"

class ChunkManager;

/// @namespace voxel_collision
/// @brief Пространство имён для систем воксельной физики и коллизий.
/// Содержит резолвер столкновений на основе sweep-алгоритма с sub-stepping,
/// обеспечивающий стабильную работу при высоком FPS и предотвращающий туннелирование.
namespace voxel_collision
{

// ============================================================================
/** @name Физические параметры игрока
 *  @{ */
// ============================================================================

/// @struct PlayerCapsule
/// @brief Представление игрока как капсулы для расчёта коллизий.
/// 
/// @par Механизм:
/// Капсула задаётся радиусом и высотой, позиция отслеживается по нижней точке (_bottomPos).
/// Такая модель обеспечивает плавное скольжение по углам блоков (в отличие от AABB)
/// и упрощает расчёт столкновений за счёт проверки точек по периметру радиуса.
/// 
/// @note Все размеры в мировых единицах (1 единица = 1 блок мира).
struct PlayerCapsule
{
    float radius = 0.3f;              ///< Радиус капсулы в горизонтальной плоскости
    float height = 1.8f;              ///< Полная высота капсулы от подошвы до макушки
    ax::Vec3 bottomPos = ax::Vec3::ZERO; ///< Мировая позиция нижней точки капсулы (опорная точка)
};

/// @} // конец группы "Физические параметры игрока"

// ============================================================================
/** @name Результат разрешения коллизий
 *  @{ */
// ============================================================================

/// @struct CollisionResult
/// @brief Структура результата работы VoxelCollisionResolver::resolve().
/// 
/// @par Механизм передачи данных:
/// Возвращается из resolve() и используется в FirstPersonController для:
/// - синхронизации позиции камеры с разрешённой позицией капсулы;
/// - обновления флага isGrounded для логики прыжков и ускорения;
/// - обработки hitCeiling для остановки восходящей скорости при ударе о потолок;
/// - вычисления нормали столкновения для механики wall sliding (скольжение вдоль стен).
/// 
/// @note Все поля инициализируются значениями по умолчанию — безопасный no-op при раннем return.
struct CollisionResult
{
    ax::Vec3 resolvedPosition = ax::Vec3::ZERO;  ///< Позиция капсулы после разрешения всех коллизий
    bool     isGrounded = false;                 ///< Флаг: касается ли капсула твёрдой поверхности снизу
    ax::Vec3 collisionNormal = ax::Vec3::ZERO;   ///< Нормаль последнего столкновения (для скольжения)
    
    /// @brief Флаг: произошло ли столкновение с потолком в текущем кадре
    /// @par Зачем нужно:
    /// При движении вверх (прыжок, лифт) важно обнулить вертикальную скорость,
    /// чтобы игрок не «застревал» в потолке. Флаг сохраняется независимо от
    /// последующих проходов по осям X/Z, которые могут перезаписать collisionNormal.
    bool     hitCeiling = false;
};

/// @} // конец группы "Результат разрешения коллизий"

// ============================================================================
/** @name Основной резолвер коллизий
 *  @{ */
// ============================================================================

/// @class VoxelCollisionResolver
/// @brief Sweep-based резолвер коллизий для воксельного мира.
/// 
/// @par Архитектура и алгоритм:
/// Реализует последовательное разрешение коллизий по осям (Y → X → Z) с sub-stepping:
/// 1. **Y-проход первым**: критично для корректной работы гравитации и прыжков.
///    Устанавливает isGrounded и hitCeiling до обработки горизонтальных осей.
/// 2. **Sub-stepping**: движение разбивается на шаги ≤0.5 блока, что гарантирует
///    детектирование столкновений даже при высокой скорости (защита от туннелирования).
/// 3. **Skin width**: проверка коллизий выполняется с отступом (radius - 0.02f)
///    от геометрической границы капсулы — предотвращает застревание на углах блоков.
/// 4. **Ground probe**: после основных проходов выполняется дополнительная проверка
///    точки чуть ниже подошвы (−0.1 единицы) — стабилизирует isGrounded при высоком FPS,
///    когда шаг движения за кадр меньше минимального смещения в checkCollisionAt.
/// 
/// @par Потокобезопасность:
/// - Все публичные методы вызываются только из главного потока.
/// - Внутренние запросы к ChunkManager выполняются через const-ссылку (только чтение).
/// - Нет мутабельного состояния, разделяемого между потоками.
/// 
/// @threadsafe Публичный API безопасен при вызове из главного потока.
///          Внутренняя логика предполагает однопоточное использование.
class VoxelCollisionResolver
{
public:
    // ========================================================================
    /** @name Конструктор и основной API
     *  @{ */
    // ========================================================================

    /// @brief Конструктор резолвера
    /// @param chunkMgr Указатель на менеджер чанков для запросов блоков
    /// @note Ссылка хранится как const — резолвер только читает данные мира,
    ///       не модифицирует состояние ChunkManager.
    explicit VoxelCollisionResolver(const ChunkManager* chunkMgr);

    /// @brief Главный метод разрешения коллизий за кадр
    /// @param dt Дельта времени с прошлого кадра (секунды)
    /// @param velocity Текущий вектор скорости игрока (до применения коллизий)
    /// @param capsule Ссылка на капсулу игрока (позиция обновляется по ссылке)
    /// @return Структура CollisionResult с разрешённой позицией и флагами состояния
    /// 
    /// @par Алгоритм работы:
    /// 1. Валидация входных данных (dt > 0, chunkMgr != nullptr).
    /// 2. **Y-проход**: вызов sweepAxis() для вертикального движения.
    ///    - Если обнаружено столкновение снизу → isGrounded = true.
    ///    - Если обнаружено столкновение сверху → hitCeiling = true (сохраняется).
    /// 3. **X и Z проходы**: горизонтальное движение с расчётом collisionNormal.
    /// 4. **Ground probe**: дополнительная проверка точки под подошвой,
    ///    если isGrounded остался false — корректировка для граничных случаев.
    /// 5. Обновление позиции капсулы и возврат результата.
    /// 
    /// @note Порядок осей (Y первым) критичен: если сначала обработать X/Z,
    ///       игрок может «проскользнуть» мимо края платформы при падении.
    CollisionResult resolve(float dt, const ax::Vec3& velocity, PlayerCapsule& capsule);

    /// @} // конец группы "Конструктор и основной API"

private:
    // ========================================================================
    /** @name Внутренние утилиты
     *  @{ */
    // ========================================================================

    /// @brief Проверка: является ли точка в мире твёрдым блоком
    /// @param worldPos Мировые координаты для проверки
    /// @return true если блок ≠ BLOCK_AIR, false если воздух или чанк не загружен
    /// 
    /// @par Механизм:
    /// Делегирует запрос в ChunkManager::getBlockAtWorldPos().
    /// Возвращает false для любых ошибок (неактивный чанк, выход за границы) —
    /// это безопасно: «неизвестная» область считается проходимой, что предотвращает
    /// ложные коллизии на границах загруженной зоны.
    bool isSolidAt(const ax::Vec3& worldPos) const;

    /// @brief Развёртка (sweep) вдоль одной оси с sub-stepping
    /// @param dt Дельта времени
    /// @param velocity Исходный вектор скорости
    /// @param capsule Капсула игрока (позиция обновляется инкрементально)
    /// @param axis Индекс оси: 0 = X, 1 = Y, 2 = Z
    /// @param grounded Ссылка на флаг заземления (обновляется при столкновении снизу)
    /// @param outResult Структура для накопления результата (нормаль, позиция)
    /// 
    /// @par Алгоритм sub-stepping:
    /// 1. Вычисление общего смещения: moveDelta = velocity[axis] * dt.
    /// 2. Ограничение |moveDelta| ≤ MAX_DELTA (защита от лагов и пролёта сквозь стены).
    /// 3. Разбиение на шаги: steps = ceil(|moveDelta| / SWEEP_STEP), где SWEEP_STEP = 0.5.
    /// 4. Инкрементальная проверка каждого шага через лямбду checkCollisionAt:
    ///    - Проверяются 4 уровня по Y (включая −0.05 для high-FPS стабильности).
    ///    - На каждом уровне: центр + 4 угла с учётом skin width (radius − 0.02).
    /// 5. При обнаружении коллизии:
    ///    - Вычисляется normal для скольжения (направлен против движения).
    ///    - Если ось Y и движение вниз → grounded = true.
    ///    - Цикл прерывается, позиция не обновляется (остановка у стены).
    /// 6. Если коллизии нет → принимается шаг, позиция обновляется.
    /// 
    /// @note Для оси Y при движении вниз: grounded сбрасывается на каждом шаге,
    ///       и устанавливается только при реальном столкновении — это позволяет
    ///       корректно детектировать «отрыв» от земли при сходе с уступа.
    void sweepAxis(float dt, const ax::Vec3& velocity, PlayerCapsule& capsule,
                   int axis, bool& grounded, CollisionResult& outResult);

    /// @} // конец группы "Внутренние утилиты"

    // ========================================================================
    /** @name Члены данных
     *  @{ */
    // ========================================================================

    const ChunkManager* _chunkMgr;  ///< Ссылка на менеджер чанков (только чтение)

    /// @brief Порог для сравнения с нулём (защита от плавающей погрешности)
    /// @note Используется для пропуска осей с нулевой скоростью и обнуления малых скоростей
    static constexpr float EPSILON = 1e-4f;

    /// @} // конец группы "Члены данных"
};

/// @} // конец группы "Основной резолвер коллизий"

} // namespace voxel_collision
