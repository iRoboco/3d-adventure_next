/**
 * @file ChunkManager.h
 * @brief Менеджер чанков для воксельного мира в стиле Minecraft
 * 
 * Отвечает за жизненный цикл чанков: генерацию, загрузку, визуализацию и выгрузку.
 * Использует многопоточную архитектуру для асинхронной генерации воксельных данных
 * и потокобезопасную передачу результатов в главный поток для отрисовки.
 * 
 * @par Принцип работы:
 * - Игрок перемещается по миру → вычисляются координаты чанков в радиусе видимости
 * - Новые чанки ставятся в очередь генерации (_genQueue)
 * - Фоновые воркеры извлекают задачи, генерируют данные через _onGenerate коллбек
 * - Готовые чанки передаются в _readyChunks и визуализируются в главном потоке
 * - Чанки за пределами радиуса выгрузки помечаются и удаляются с освобождением ресурсов
 * 
 * @par Потокобезопасность:
 * - _genQueue: защищён мьютексом, используется condition_variable для ожидания задач
 * - _readyChunks: защищён _readyMtx, запись из воркеров, чтение из главного потока
 * - _chunks: доступ только из главного потока (игровой цикл)
 * - _genPendingSet: доступ только из главного потока для отслеживания ожидающих генерацию
 * 
 * @note Для паузы/возобновления используйте pause()/resume() — они сохраняют состояние мира.
 *       shutdown() вызывается только в деструкторе для полной очистки ресурсов.
 */

