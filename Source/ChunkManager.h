/**
 * @brief Уникальный идентификатор чанка по его координатам в сетке мира
 * 
 * Используется как ключ в хеш-таблицах для быстрого доступа к данным чанка.
 * Координаты (x, y, z) представляют индекс чанка в глобальной сетке,
 * а не мировые координаты в единицах игры.
 */
struct ChunkKey
{
    int32_t x, y, z; ///< Координаты чанка в сетке мира

    /// @brief Сравнение на равенство для использования в контейнерах
    bool operator==(const ChunkKey& o) const noexcept { 
        return x == o.x && y == o.y && z == o.z; 
    }
    
    /// @brief Сравнение на неравенство (инверсия operator==)
    bool operator!=(const ChunkKey& o) const noexcept { 
        return !(*this == o); 
    }
    
    /**
     * @brief Функтор хеширования для использования в std::unordered_map
     * 
     * Реализует FNV-1a 64-bit алгоритм с побайтовым смешиванием координат.
     * Обеспечивает равномерное распределение хешей для пространственных данных,
     * минимизируя коллизии при последовательных координатах чанков.
     */
    struct Hash
    {
        size_t operator()(const ChunkKey& k) const noexcept
        {
            // FNV-1a 64-bit → size_t. Быстрый и устойчивый к коллизиям хеш для координат
            uint64_t h = 14695981039346656037ULL;
            auto mix = [&](int32_t v) {
                uint64_t u = static_cast<uint64_t>(static_cast<uint32_t>(v));
                for (int i = 0; i < 4; ++i) {
                    h ^= (u >> (i * 8)) & 0xFF;
                    h *= 1099511628211ULL;
                }
            };
            mix(k.x); mix(k.y); mix(k.z);
            return static_cast<size_t>(h);
        }
    };
};

/// @brief Идентификатор блока — компактное хранение типа вокселя
using BlockId = uint16_t;

static constexpr BlockId BLOCK_AIR   = 0;  ///< Пустой блок (воздух)
static constexpr BlockId BLOCK_GRASS = 1;  ///< Блок травы (верхний слой земли)
static constexpr BlockId BLOCK_STONE = 2;  ///< Каменный блок
static constexpr BlockId BLOCK_DIRT  = 3;  ///< Блок земли

/// @name Размеры чанка в блоках
/// @{
static constexpr int CHUNK_SIZE_X = 16;  ///< Ширина чанка по оси X
static constexpr int CHUNK_SIZE_Y = 256; ///< Высота чанка по оси Y (вертикаль)
static constexpr int CHUNK_SIZE_Z = 16;  ///< Глубина чанка по оси Z
/// @}

// ============================================================================
/** @name Данные чанка
 *  @{ */
// ============================================================================

/**
 * @brief Контейнер воксельных данных одного чанка
 * 
 * Хранит линейный массив блоков размером CHUNK_SIZE_X * CHUNK_SIZE_Y * CHUNK_SIZE_Z.
 * Использует Y-major layout для оптимизации кэш-локальности при проходе по высоте.
 * 
 * @par Память: ~128 КБ на чанк (65536 блоков × 2 байта)
 * @par Индексация: lx + ly * CHUNK_SIZE_X + lz * CHUNK_SIZE_X * CHUNK_SIZE_Y
 */
class ChunkData
{
public:
    /// @brief Конструктор с инициализацией массива блоков значением BLOCK_AIR
    explicit ChunkData(const ChunkKey& key) : _key(key)
    {
        _blocks.resize(CHUNK_SIZE_X * CHUNK_SIZE_Y * CHUNK_SIZE_Z, BLOCK_AIR);
    }
    
    /// @brief Получение ключа чанка (константная ссылка)
    const ChunkKey& getKey() const noexcept { return _key; }
    
    /**
     * @brief Чтение типа блока по локальным координатам
     * @param lx Локальная координата X [0; CHUNK_SIZE_X)
     * @param ly Локальная координата Y [0; CHUNK_SIZE_Y)
     * @param lz Локальная координата Z [0; CHUNK_SIZE_Z)
     * @return BlockId типа блока или BLOCK_AIR при выходе за границы
     */
    BlockId getBlock(int lx, int ly, int lz) const noexcept { 
        return _blocks[index(lx, ly, lz)]; 
    }
    
    /**
     * @brief Запись типа блока по локальным координатам
     * @param lx Локальная координата X [0; CHUNK_SIZE_X)
     * @param ly Локальная координата Y [0; CHUNK_SIZE_Y)
     * @param lz Локальная координата Z [0; CHUNK_SIZE_Z)
     * @param id Идентификатор блока для записи
     */
    void setBlock(int lx, int ly, int lz, BlockId id) noexcept { 
        _blocks[index(lx, ly, lz)] = id; 
    }
    
    /// @brief Проверка: состоит ли чанк полностью из воздуха (оптимизация рендера)
    bool isAllAir() const noexcept { 
        return std::all_of(_blocks.begin(), _blocks.end(), 
                          [](BlockId b){ return b == BLOCK_AIR; }); 
    }
    
    /// @brief Доступ к сырому массиву блоков для прямого доступа (осторожно!)
    std::vector<BlockId>& rawBlocks() noexcept { return _blocks; }
    
private:
    /**
     * @brief Расчёт линейного индекса в массиве блоков
     * @par Формула: lx + ly * CHUNK_SIZE_X + lz * CHUNK_SIZE_X * CHUNK_SIZE_Y
     * @note Y-major layout: изменение Y даёт последовательный доступ к памяти
     */
    static size_t index(int lx, int ly, int lz) noexcept
    {
        return static_cast<size_t>(lx + ly * CHUNK_SIZE_X + lz * CHUNK_SIZE_X * CHUNK_SIZE_Y);
    }
    
    ChunkKey _key; ///< Ключ чанка для идентификации
    std::vector<BlockId> _blocks; ///< Линейный массив воксельных данных
};

/// @brief Статус жизненного цикла чанка в менеджере
enum class ChunkStatus { 
    None,              ///< Чанк не инициализирован / удалён
    QueuedForGen,      ///< Ожидает генерации в очереди воркеров
    Active,            ///< Сгенерирован и отображается в сцене
    QueuedForUnload    ///< Помечен на выгрузку, ожидает обработки
};

/// @} // конец группы "Данные чанка"

